(function($) {

   $.fn.zoomer = function( options ) {
        // Default settings
        var settings = $.extend({
            // do setting
            m_viewport : 80,
        }, options);

        this.addClass("img-zoomin");

        return this.click( function() {

            var img = $(this);
            var wrap = "<div class='wrap-zoom'></div>";

            var img_w = img.get(0).naturalWidth;
            var img_h = img.get(0).naturalHeight;
            var this_w = img.width();
            var this_h = img.height();

            var scrollTop = $(window).scrollTop();

            var scale_factor = img_w / this_w;
            var margin_viewport = settings.m_viewport;
            var viewportWidth = ($(window).width() - margin_viewport);
            var viewportHeight = ($(window).height() - margin_viewport*1.5);

            var imgRatio = img_w / img_h;
            var viewportRatio = viewportWidth / viewportHeight;
            
            // ZOOM SCALE FACTOR
            if (img_w < viewportWidth && img_h < viewportHeight) {
              img_scale = scale_factor;

            } else if (imgRatio < viewportRatio) {
              img_scale = (viewportHeight / img_h) * scale_factor

            } else {
              img_scale = (viewportWidth / img_w) * scale_factor
            }
            // 

            // OFFSET POSITION
            var img_offset = img.offset();

            var viewportY = scrollTop + ($(window).height() / 2);
            var viewportX = ($(window).width() / 2);

            var imageCenterY = img_offset.top + (this_h / 2);
            var imageCenterX = img_offset.left + (this_w / 2);

            var translateY = viewportY - imageCenterY - 30;
            var translateX = viewportX - imageCenterX;

            var imageWrapTransform = 'translate3d(' + translateX + 'px, ' + translateY + 'px, 0)';

            // jika ada link
            var btn_link = img.siblings(".detail-btn");

            function zoomOut() {
                $("html").css({
                    "overflow": ""
                });
                imgZoomed = $(".img-zoomout");
                if($("body").hasClass("zoom-overlay-open")) {
                    $("body").removeClass("zoom-overlay-open");
                    $(".capzoom").remove();
                    $(".wrap-zoom").parents(".item").css({
                        "z-index": 101
                    });
                    setTimeout(function() {
                        // $("body").removeClass("overflow-x-hidden");
                        $(".zoom-overlay").remove();
                        $(".wrap-zoom").parents(".wrap").removeClass("waw");
                        imgZoomed.unwrap().addClass("img-zoomin").removeClass("img-zoomout");
                    },600);
                    imgZoomed.removeAttr("style");
                    imgZoomed.parent(".wrap-zoom").css({
                        "-webkit-transform": "",
                            "-ms-transform": "",
                                "transform": ""
                    });
                    $(".wrap").css({
                        "pointer-events": "",
                        "opacity": "",
                        "transform": ""
                    });
                    $(".bulan").css({
                        "filter": ""
                    })
                }    
            }

            function zoomIn() {
                img.wrap(wrap).removeClass("img-zoomin").addClass("img-zoomout");
                $("html").css({
                    "overflow": "hidden"
                })
                // $("body").addClass("overflow-x-hidden");
                $("body").append("<div class='zoom-overlay'></div>");
                
                $(".wrap-zoom").parents(".item").css({
                    "z-index": 999
                });
                
                setTimeout(function() {
                    $(".wrap-zoom").parents(".wrap").addClass("waw");
                    $("body").addClass("zoom-overlay-open")
                    img.css({
                        "transform": "scale(" + img_scale + ")",
                    });
                    img.parent(".wrap-zoom").css({
                        "-webkit-transform": imageWrapTransform,
                            "-ms-transform": imageWrapTransform,
                                "transform": imageWrapTransform
                    });
                },5);
                if(btn_link.length) {
                    btn_link.clone().addClass("capzoom").wrapInner("<span></span>").appendTo("body");
                }
                $(".wrap").css({
                    "pointer-events": "none",
                    "opacity": 0,
                    "transform": "scale(1.5)"
                });
                $(".wrap--tl").css({
                    "transform": "scale(1.5) translate3d(-200px, -100px, 0px)"
                });
                $(".wrap--bl").css({
                    "transform": "scale(1.5) translate3d(-200px, 100px, 0px)"
                });
                $(".wrap--br").css({
                    "transform": "scale(1.5) translate3d(200px, 100px, 0px)"
                });
                $(".wrap--tr").css({
                    "transform": "scale(1.5) translate3d(200px, -100px, 0px)"
                });
                $(".wrap--tc").css({
                    "transform": "scale(1.5) translate3d(0px, -100px, 0px)"
                });
                $(".wrap--bc").css({
                    "transform": "scale(1.5) translate3d(0px, 100px, 0px)"
                })
                $(".bulan").css({
                    "filter": "blur(7px)"
                })
            }

            // EVENTS
            if(img.hasClass("img-zoomout")) {
                zoomOut();
            }
            else {
                zoomIn();
            }

            $(".zoom-overlay, .wrap-zoom").click(function() {
                zoomOut();
            });
            
            $(document).keydown(function(e) {
                // ESCAPE key pressed
                if (e.keyCode == 27) {
                    zoomOut();
                }
            });

            window.addEventListener("scroll", zoomOut);

        });

    }

}(jQuery));

